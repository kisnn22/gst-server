def upload():
    global latest_image, processed_latest_image
    try:
        image = request.get_data()
        latest_image = image # Save what the ESP32 sent us exactly

        if not image:
            return jsonify({"status": "ERROR", "msg": "No image data sent"})

        # --- STEP 1: Process and flatten the image first! ---
        has_shape, processed_image = crop_invoice(image)
        processed_latest_image = processed_image # Save the OpenCV result

        if has_shape == "BLUR":
            print("❌ Image was too blurry. Aborting Vision API!")
            return jsonify({"status": "BLUR"})

        # --- STEP 2: Feed the perfectly flat, cropped image to OCR ---
        text = extract_text(processed_image)
        
        # 🟢 DEBUGGING: Print exactly what OCR saw!
        print("====================================")
        print("🔍 RAW OCR TEXT EXTRACTED:")
        print(text if len(text) > 0 else "[OCR API RETURNED BLANK OR CRASHED]")
        print("====================================")

        # --- STEP 3: Validate the text ---
        if not is_invoice(text):
            safe_text = (text[:150] + '...') if len(text) > 150 else text
            # Sanitize for simpler JSON parsing
            safe_text = safe_text.replace("\n", " ").replace("\r", "").replace("\"", "'")
            return jsonify({"status": "NOT_INVOICE", "text": safe_text})

        gst = find_gst(text)

        if not gst:
            return jsonify({"status": "GST_MISSING"})

        try:
            # --- STEP 4: Save to Firebase ---
            ref = db.reference("GST_HISTORY")
            data = ref.get() or {}

            if gst in data:
                return jsonify({"status": "DUPLICATE_GST"})

            ref.child(gst).set({"ok": True})
            
        except Exception as fb_error:
            # If Google Vision worked, but Firebase failed, catch it here!
            print("FIREBASE CRASH LOG:", str(fb_error))
            return jsonify({"status": "FIREBASE_CRASH", "gst": gst, "error": str(fb_error)})

        return jsonify({"status": "VALID_INVOICE", "gst": gst})
        
    except Exception as e:
        print("VISION CRASH LOG:", traceback.format_exc())
        return jsonify({"status": "PYTHON_CRASH", "error": str(e)})


