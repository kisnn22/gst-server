# GST Extraction
def find_gst(text):
    # Try strict exact match first
    match = re.findall(r"\d{2}[A-Z]{5}\d{4}[A-Z]\d[Z][A-Z\d]", text)
    if match: return match[0]
    
    # Blurry digital text often confuses OCR kerning, causing it to insert random
    # spaces inside continuous strings (e.g., "27AA B CF 8078").
    # We remove ALL whitespace characters to form one giant continuous string block.
    clean_text = re.sub(r'\s+', '', text)
    
    # Try strict match on the spaceless text
    match_clean = re.findall(r"\d{2}[A-Z]{5}\d{4}[A-Z]\d[Z][A-Z\d]", clean_text)
    if match_clean: return match_clean[0]
    
    # Fallback: Look for the literal word "GSTIN". 
    # We use [^A-Za-z0-9]* to capture and ignore any random punctuation (like colons, semicolons, dashes) 
    # that the OCR might have incorrectly interpreted between "GSTIN" and the number.
    fallback = re.search(r"GSTIN[^A-Za-z0-9]*([A-Za-z0-9\u00df]{12,18})", clean_text, re.IGNORECASE)
    if fallback:
        extracted = fallback.group(1).upper()
        # Replace common OCR misreads
        extracted = extracted.replace("ß", "B")
        return extracted
        
    return None

# 🔥 BULLETPROOF AUTO-ZOOM & BLUR DETECTION
def crop_invoice(image_bytes):
    try:
        nparr = np.frombuffer(image_bytes, np.uint8)
        img = cv2.imdecode(nparr, cv2.IMREAD_COLOR)

        if img is None:
            return False, image_bytes

        # Grayscale conversion
        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        
        # 🟢 VERIFY IMAGE IS NOT BLURRY (Matches ESP32 logic)
        variance = cv2.Laplacian(gray, cv2.CV_64F).var()
        print(f"📷 Image Sharpness Variance: {variance:.2f}") # Helps debugging on Render
        
        # ESP32-CAM OV2640 lenses are naturally a bit soft. 
        # A threshold of 10 is much more forgiving than 40, especially for phone screens.
        if variance < 10.0:
            return "BLUR", image_bytes

        # Apply slight blur just for edge detection algorithms
        gray_blurred = cv2.GaussianBlur(gray, (5, 5), 0)
        edges = cv2.Canny(gray_blurred, 75, 200)

        cnts = cv2.findContours(edges, cv2.RETR_LIST, cv2.CHAIN_APPROX_SIMPLE)
        contours = cnts[0] if len(cnts) == 2 else cnts[1]
        
        contours = sorted(contours, key=cv2.contourArea, reverse=True)[:5]
        
        screen_cnt = None
        for cnt in contours:
            peri = cv2.arcLength(cnt, True)
            approx = cv2.approxPolyDP(cnt, 0.02 * peri, True)
            if len(approx) == 4 and cv2.contourArea(cnt) > 5000:
                screen_cnt = approx
                break
                
        if screen_cnt is None:
            return False, image_bytes
            
        pts = screen_cnt.reshape(4, 2)
        rect = np.zeros((4, 2), dtype="float32")
        s = pts.sum(axis=1)
        rect[0] = pts[np.argmin(s)]
        rect[2] = pts[np.argmax(s)]
        diff = np.diff(pts, axis=1)
        rect[1] = pts[np.argmin(diff)]
        rect[3] = pts[np.argmax(diff)]

        (tl, tr, br, bl) = rect

        widthA = np.sqrt(((br[0] - bl[0]) ** 2) + ((br[1] - bl[1]) ** 2))
        widthB = np.sqrt(((tr[0] - tl[0]) ** 2) + ((tr[1] - tl[1]) ** 2))
        maxWidth = max(int(widthA), int(widthB))

        heightA = np.sqrt(((tr[0] - br[0]) ** 2) + ((tr[1] - br[1]) ** 2))
        heightB = np.sqrt(((tl[0] - bl[0]) ** 2) + ((tl[1] - bl[1]) ** 2))
        maxHeight = max(int(heightA), int(heightB))

        dst = np.array([
            [0, 0],
            [maxWidth - 1, 0],
            [maxWidth - 1, maxHeight - 1],
            [0, maxHeight - 1]], dtype="float32")

        M = cv2.getPerspectiveTransform(rect, dst)
        warped = cv2.warpPerspective(img, M, (maxWidth, maxHeight))

        warped_gray = cv2.cvtColor(warped, cv2.COLOR_BGR2GRAY)
        
        is_success, buffer = cv2.imencode(".jpg", warped_gray)
        if is_success:
            return True, buffer.tobytes()
            
    except Exception as e:
        print("Safely ignoring crop error:", e)
        
    return False, image_bytes

